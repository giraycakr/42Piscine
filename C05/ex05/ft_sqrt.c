int	ft_sqrt(int nb)
{
	long int	b;
	long int	i;

	b = nb;
	i = 2;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	while (i * i <= b)
	{
		if (i * i == b)
			return (i);
		i++;
	}
	return (0);
}
